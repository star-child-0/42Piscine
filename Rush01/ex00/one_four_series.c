void	one_four_series(int i, char mat[4][4], int k)
{
	if (i < 7)
	{
		mat[3][k] = '4';
		mat[2][k] = '3';
		mat[1][k] = '2';
	}
	if (i > 7 && i < 15)
	{
		mat[0][k] = '3';
		mat[1][k] = '2';
		mat[2][k] = '1';
	}
}
