int	check_string_format(char *str)
{
	int	i;
	int	r;

	i = 0;
	r = 0;
	if (!(str[0] >= '1' && str[0] <= '4'))
		return (0);
	while (str[i] != '\0')
	{
		if ((str[i] >= '1' && str[i] <= '4') && (str[i + 1] == ' '
				|| str[i + 1] == '\0'))
			r = 1;
		else if (str[i] == ' ' && ((str[i + 1] >= '1' && str[i + 1] <= '4')
				|| (str[i - 1] >= '1' && str[i - 1] <= '4')
				|| str[i + 1] == '\0'))
			r = 1;
		else
			return (0);
		i++;
	}
	if (i == 31 && r == 1)
		return (1);
	else
		return (0);
}
